public class InputPair {
    double[] input;

    public double[] getInput() {
        return input;
    }

    public double[] getOutput() {
        return output;
    }

    double[] output;


    public InputPair(double[] input, double[] output) {
        this.input = input;
        this.output = output;
    }
}
